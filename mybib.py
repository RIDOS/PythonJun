def sum(a, b):
    return a+b

def raz(a, b):
    return a-b

def mult(a, b):
    return a*b

def delen(a, b):
    return a/b

def modul(a):
    return abs(a)

def fac(a):
    fac = 1
    i = 0
    while i < a:
        i += 1
        fac = fac * i
    return fac











